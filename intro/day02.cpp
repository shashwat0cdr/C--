// Variables and Datatypes

#include <iostream>
using namespace std;

int main() {
    // Declaring variables
    int age = 25;
    float height = 5.8;
    char grade = 'A';
    bool isStudent = true;

    // Printing variable values
    cout << "Age: " << age << endl;
    cout << "Height: " << height << " foot" << endl;
    cout << "Grade: " << grade << endl;
    cout << "Is Student: " << isStudent << endl;

    return 0;
}